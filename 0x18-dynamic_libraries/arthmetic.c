/**
 * mul - mul two nums
 * @a: first
 * @b: second
 * Return: result
 */
int mul(int a, int b)
{
	return (a * b);
}

/**
 * add - add two nums
 * @a: first
 * @b: second
 * Return: sum
 */

int add(int a, int b)
{
	return (a + b);
}

/**
 * div - divides two numbers
 * @a: first
 * @b: second
 * Return: result
 */

int div(int a, int b)
{
	return (a / b);
}


/**
 * sub - difference between two nums
 * @a: first
 * @b: second
 * Return: result
 */

int sub(int a, int b)
{
	return (a - b);
}

/**
 * mod - mod of two nums
 * @a: first
 * @b: second
 * Return: result
 */

int mod(int a, int b)
{
	return (a % b);
}
